class Solution(object):
    def depthSum(self, nestedList):
        """
        :type nestedList: List[NestedInteger]
        :rtype: int
        """
        return self.rec(nestedList,1)
    
    def rec(self, nl, w):
        ret = 0
        for i in nl:
            ret += ((w * i.getInteger()) if i.isInteger() else self.rec(i.getList(), w + 1))
        return ret
            